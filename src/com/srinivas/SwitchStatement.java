package com.srinivas;

public class SwitchStatement {

	public static void main(String[] args) {
		String str = "srinivas";
		switch(str) {
		case "yadav":
			System.out.println("1");
			break;
		case "nobitha":
			System.out.println("2");
			break;
		case "srinivas":
			System.out.println("3");
			break;
		default:
			System.out.println("default value");
			break;
			
			
		}
	}

}
